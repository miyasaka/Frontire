SELECT
  PA.ANO
 ,CASE WHEN LENGTH(PA.TITLE) > 20 THEN SUBSTR(PA.TITLE,0,21) || '・・・' ELSE PA.TITLE END AS STITLE
 ,PA.TITLE
 ,MIN(PC.ENTDATE) AS ENTDATE
 ,COUNT(PC.MID) AS COMCNT
FROM
  PHOTO_ALBUM PA,
  PHOTO_COMMENT PC
WHERE
     PA.MID      = /*mid*/
 AND PA.ANO      = PC.ANO
 AND PC.MID      = PA.MID
 AND PC.ENTID   != /*mid*/
 AND PC.READFLG  = '0'
 AND PC.DELFLG   = '0'
GROUP BY PA.ANO,STITLE,PA.TITLE
ORDER BY ENTDATE