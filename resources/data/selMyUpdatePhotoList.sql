SELECT
  PA.MID
 ,PA.ANO
 ,PA.TITLE
 ,TO_CHAR(PA.ENTDATE,'yyyy年MM月DD日 hh24:mi') AS ENTDATE
 ,PC.COMMENTS
FROM
  PHOTO_ALBUM PA
  LEFT JOIN (SELECT MID,ANO,COUNT(MID) AS COMMENTS
             FROM PHOTO_COMMENT
             WHERE DELFLG = '0'
             GROUP BY MID,ANO) PC ON PA.MID = PC.MID AND PA.ANO = PC.ANO
WHERE
     PA.MID    = /*mid*/ 
 AND PA.DELFLG = '0'
ORDER BY
  PA.ENTDATE DESC
