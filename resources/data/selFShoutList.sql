SELECT
 A.MID,
 A.NICKNAME,
 A.NO,
 A.TWITTER,
 A.ENTID,
 A.ENTDATE,
 A.COMMENT,
 CASE
  WHEN A.YD < TO_CHAR(A.ENTDATE,'YYYYMMDD') THEN TO_CHAR(A.ENTDATE,'HH24:MI')
  ELSE TO_CHAR(A.ENTDATE,'MM/DD HH24:MI')
  END AS ENTDATE,
 A.PHOTO,
 COALESCE((SELECT COMMENT AS MYCOMMENT FROM FRONTIERSHOUT WHERE MID = /*mymid*/ ORDER BY ENTDATE DESC LIMIT 1),'') AS MYCOMMENT
FROM
(
SELECT
 M.MID,
 M.NICKNAME,
 FS.COMMENT,
 FS.NO,
 FS.TWITTER,
 FS.ENTID,
 FS.ENTDATE,
 TO_CHAR(CURRENT_TIMESTAMP + '-1DAYS','YYYYMMDD') AS YD,
 CASE mpi.MAINPICNO WHEN '1' THEN mpi.PIC1 WHEN '2' THEN mpi.PIC2 WHEN '3' THEN mpi.PIC3 END AS PHOTO
FROM
 MEMBERS M,
 FRONTIERSHOUT FS,
 MEMBERPHOTO_INFO MPI
WHERE
     FS.MID IN /*mid*/(1,2)
 AND M.MID = FS.MID
 AND M.MID = MPI.MID
 AND M.STATUS                   = '1'
GROUP BY
 M.MID,
 M.NICKNAME,
 FS.COMMENT,
 FS.NO,
 FS.TWITTER,
 FS.ENTID,
 FS.ENTDATE,
 PHOTO
ORDER BY FS.ENTDATE DESC
) A