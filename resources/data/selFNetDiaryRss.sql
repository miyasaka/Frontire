SELECT
 A.MID,
 A.DIARYID,
 A.COMNO,
 A.TITLE || '(' || B.NICKNAME || ')' AS TITLE,
 TO_CHAR(A.ENTDATE,'YYYYMMDDHH24MISS') AS ENTDATE,
 A.APP_STATUS,
 A.DIARYCATEGORY,
 B.NICKNAME
FROM
 DIARY AS A,
 MEMBERS AS B
WHERE
 A.COMNO = 0 AND
 A.DELFLG = '0' AND 
 A.APP_STATUS IN /*appstatus*/(1,2) AND
 A.MID = B.MID AND
 B.STATUS = '1'
ORDER BY A.ENTDATE DESC