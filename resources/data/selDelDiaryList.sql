SELECT
    TITLE,
	TO_CHAR(ENTDATE,'YYYYMMDDHH24MI') AS ENTDATE,
	COMMENT,
	PIC1,
	PIC2,
	PIC3,
	COALESCE(PICNOTE1,'') AS PICNOTE1,
	COALESCE(PICNOTE2,'') AS PICNOTE2,
	COALESCE(PICNOTE3,'') AS PICNOTE3
FROM
    DIARY
WHERE
    MID =/*mid*/ AND
    COMNO = /*comno*/ AND
    DIARYID IN /*diaryid*/(1,2)
ORDER BY
	ENTDATE DESC;
