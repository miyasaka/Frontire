SELECT
 A.COMMENT,
 A.DEFENTDATE,
 CASE WHEN A.ENTDATE < 5 THEN '約5分前'
 WHEN A.ENTDATE < 55 THEN '約'||(TRUNC(A.ENTDATE/5 + .9, 0)*5)||'分前'
 WHEN A.ENTDATE < 60*23 THEN '約'||CEIL(A.ENTDATE/60)||'時間前'
 WHEN A.ENTDATE < 60*24 THEN '1日以内'
 WHEN A.ENTDATE < 60*72 THEN TRUNC(A.ENTDATE/60/24)+1||'日以内'
 ELSE '3'||'日以上前'
 END AS ENTDATE
FROM
(SELECT
 COMMENT,
 ENTDATE AS DEFENTDATE,
CASE TO_NUMBER(TO_CHAR(CURRENT_TIMESTAMP - ENTDATE,'DDD'),'99999')
 WHEN 0 THEN TO_NUMBER(TO_CHAR(CURRENT_TIMESTAMP - ENTDATE,'HH24'),'9999')*60+TO_NUMBER(TO_CHAR(CURRENT_TIMESTAMP - ENTDATE,'MI'),'99')
 ELSE TO_NUMBER(TO_CHAR(CURRENT_TIMESTAMP - ENTDATE,'DDD'),'99999')*60*24
 END AS ENTDATE
FROM
 FRONTIERSHOUT
WHERE
 MID = /*mid*/
) A
ORDER BY DEFENTDATE DESC
limit 1