SELECT
 MAX(TO_CHAR(A.ENTDATE,'YYYYMMDD HH24:MI:SS')) AS MAXENTDATE
FROM
(
SELECT
 M.MID,
 M.NICKNAME,
 FS.COMMENT,
 FS.NO,
 FS.TWITTER,
 FS.ENTID,
 FS.ENTDATE,
 TO_CHAR(CURRENT_TIMESTAMP + '-1DAYS','YYYYMMDD') AS YD,
 CASE mpi.MAINPICNO WHEN '1' THEN mpi.PIC1 WHEN '2' THEN mpi.PIC2 WHEN '3' THEN mpi.PIC3 END AS PHOTO,
 CASE TO_NUMBER(TO_CHAR(CURRENT_TIMESTAMP - FS.ENTDATE,'DDD'),'99999')
  WHEN 0 THEN TO_NUMBER(TO_CHAR(CURRENT_TIMESTAMP - FS.ENTDATE,'HH24'),'9999')*60+TO_NUMBER(TO_CHAR(CURRENT_TIMESTAMP - FS.ENTDATE,'MI'),'99')
  ELSE TO_NUMBER(TO_CHAR(CURRENT_TIMESTAMP - FS.ENTDATE,'DDD'),'99999')*60*24
  END AS ENTDATE_L,
 TO_NUMBER(SUBSTR(M.MID, 2, 10),'000000000') AS SUBSTRMID,
 FUM.FRONTIERDOMAIN,
 FS.UPDID,
 FS.UPDDATE,
 FS.DEMANDFLG,
 FS.CONFIRMFLG,
 FS.PUB_LEVEL,
 FS.DELFLG,
 M.MEMBERTYPE,
 FUM.NICKNAME AS FNICKNAME,
 FUM.FID,
 FUM.PIC AS FPIC,
 TO_NUMBER(SUBSTR(FUM.FID, 2, 10),'000000000') AS SUBSTRFID
FROM
 MEMBERS M,
 FRONTIERSHOUT FS,
 MEMBERPHOTO_INFO MPI,
 FRONTIER_USER_MANAGEMENT FUM
WHERE
     FS.MID IN ('m000000093', 'm000000184', 'm000000176', 'm000000010')
 AND M.MID = FS.MID
 AND M.MID = MPI.MID
 AND M.MID = FUM.MID
 AND M.STATUS                   = '1'
 AND FS.DELFLG                  = '0'
 --AND FS.ENTDATE                 > (CURRENT_DATE - 7)
GROUP BY
 M.MID,
 M.NICKNAME,
 FS.COMMENT,
 FS.NO,
 FS.TWITTER,
 FS.ENTID,
 FS.ENTDATE,
 PHOTO,
 FUM.FRONTIERDOMAIN,
 FS.UPDID,
 FS.UPDDATE,
 FS.DEMANDFLG,
 FS.CONFIRMFLG,
 FS.PUB_LEVEL,
 FS.DELFLG,
 M.MEMBERTYPE,
 FUM.NICKNAME,
 FUM.FID,
 FUM.PIC
ORDER BY FS.ENTDATE DESC
) A